    /**
     * State check needed by ScheduledThreadPoolExecutor to
     * enable running tasks during shutdown.
     *
     * @param shutdownOK true if should return true if SHUTDOWN
     */
    final boolean isRunningOrShutdown(boolean shutdownOK) {
        int rs = runStateOf(ctl.get());
        return rs == RUNNING || (rs == SHUTDOWN && shutdownOK);
    }

    /**
     * Drains the task queue into a new list, normally using
     * drainTo. But if the queue is a DelayQueue or any other kind of
     * queue for which poll or drainTo may fail to remove some
     * elements, it deletes them one by one.
     */
    private List<Runnable> drainQueue() {
        BlockingQueue<Runnable> q = workQueue;
        ArrayList<Runnable> taskList = new ArrayList<Runnable>();
        q.drainTo(taskList);
        if (!q.isEmpty()) {
            for (Runnable r : q.toArray(new Runnable[0])) {
                if (q.remove(r))
                    taskList.add(r);
            }
        }
        return taskList;
    }

    /**
     * 反正就是用于添加worker的
     * 通过当前线程池的状态，以及给定的容量（要么是核心容量或者是最大容量）
     * 来判断能否新增一个worker。如果是这样的话，一个worker数量将会被调整，
     * 或者可能的话，一个新的worker将会被创建并且开始运行。运行firstTask作为
     * 它的第一个任务。如果当前线程池停止或者将要被关闭的话便会返回false。如果
     * thread factory创建线程失败也将会返回false。如果一个线程创建失败，或者
     * thread factory返回null，或者执行的时候抛出异常（一个比较典型的就是Thread.start()
     * 抛出OutOfMemoryError),将会干净的回滚回去。
     *
     * 该方法先增加worker的数量，然后再去创建worker，如果创建worker失败，再递减worker的数量
     *
     * Checks if a new worker can be added with respect to current
     * pool state and the given bound (either core or maximum). If so,
     * the worker count is adjusted accordingly, and, if possible, a
     * new worker is created and started, running firstTask as its
     * first task. This method returns false if the pool is stopped or
     * eligible to shut down. It also returns false if the thread
     * factory fails to create a thread when asked.  If the thread
     * creation fails, either due to the thread factory returning
     * null, or due to an exception (typically OutOfMemoryError in
     * Thread.start()), we roll back cleanly.
     *
     * @param firstTask the task the new thread should run first (or
     *                  null if none). Workers are created with an initial first task
     *                  (in method execute()) to bypass queuing when there are fewer
     *                  than corePoolSize threads (in which case we always start one),
     *                  or when the queue is full (in which case we must bypass queue).
     *                  Initially idle threads are usually created via
     *                  prestartCoreThread or to replace other dying workers.
     * @param core      if true use corePoolSize as bound, else
     *                  maximumPoolSize. (A boolean indicator is used here rather than a
     *                  value to ensure reads of fresh values after checking other pool
     *                  state).
     *                  如果是true,那么就用corePoolSize进行限制，如果是false那么就用maximumPoolSize进行限制
     * @return true if successful
     */
    private boolean addWorker(Runnable firstTask, boolean core) {
        // retry 是个无限循环. 当线程池处于 RUNNING (运行)状态时, 只有在线程池中
        // 的有效线程数被成功加一以后, 才会退出该循环而去执行后边的代码. 也就是说,
        // 当线程池在 RUNNING (运行)状态下退出该 retry 循环时, 线程池中的有效线程数
        // 一定少于此次设定的最大线程数(可能是 corePoolSize 或 maximumPoolSize).
        retry:
        for (; ; ) {
          //获取当前线程池数量
            int c = ctl.get();
          //获取当前运行状态
            int rs = runStateOf(c);

            // Check if queue empty only if necessary.
            //线程池的状态大于SHUTDOWN且不含以下状态，返回false
            //线程池的状态为SHUTDOWN且firstTask且workQueue不为空时，也会创建worker
            //因为SHUTDOWN状态仍然会执行已经提交的任务
            if (rs >= SHUTDOWN && !(rs == SHUTDOWN && firstTask == null && !workQueue.isEmpty())) {
              return false;
            }

            for (; ; ) {
                //计算worker数量
                int wc = workerCountOf(c);
                // 如果线程池内的有效线程数大于或等于了理论上的最大容量 CAPACITY 或者实际
                // 设定的最大容量, 就返回 false直接结束该方法. 这样同样没有创建新线程,
                // 新提交的任务也同样未被执行.
                // (core ? corePoolSize : maximumPoolSize) 表示如果 core为 true,
                // 那么实际设定的最大容量为 corePoolSize, 反之则为 maximumPoolSize.
                if (wc >= CAPACITY || wc >= (core ? corePoolSize : maximumPoolSize)) {
                  return false;
                }
                // 有效线程数加一
                if (compareAndIncrementWorkerCount(c)) {
                  break retry;
                }
              // Re-read ctl
                c = ctl.get();
                if (runStateOf(c) != rs) {
                    //线程池状态如果有改变，那么重新计算线程池状态
                  continue retry;
                }
                //如果因为workerCount的改变导致CAS失败，将再次进行循环
                // else CAS failed due to workerCount change; retry inner loop
            }
        }

        boolean workerStarted = false;
        boolean workerAdded = false;
        Worker w = null;
        try {
           //创建一个worker
            w = new Worker(firstTask);
            //worker的thread
            final Thread t = w.thread;
            if (t != null) {
                final ReentrantLock mainLock = this.mainLock;
                mainLock.lock();
                try {
                    // Recheck while holding lock.
                    // Back out on ThreadFactory failure or if
                    // shut down before lock acquired.
                   //获取锁后，将再次进行检查，检查线程池的运行状态
                    int rs = runStateOf(ctl.get());

                    //如果线程处于运行状态或者线程池处于SHUTDOWN状态并且
                    //第一个任务为NULL
                    if (rs < SHUTDOWN ||
                            (rs == SHUTDOWN && firstTask == null)) {
                        //判断线程是否处于活跃状态(活跃状态:线程已经启动并且还没结束,isAlive返回true)
                        if (t.isAlive()) // precheck that t is startable
                            throw new IllegalThreadStateException();
                        workers.add(w);
                        int s = workers.size();
                        if (s > largestPoolSize)
                            largestPoolSize = s;
                        workerAdded = true;
                    }
                } finally {
                    mainLock.unlock();
                }
                if (workerAdded) {
                    // 启动线程 t. 由于 t指向 w.thread所引用的对象, 所以相当于启动的是 w.thread所引用的线程对象.
                    // 而 w是 Runnable 的实现类, w.thread 是以 w作为 Runnable参数所创建的一个线程对象, 所以启动
                    // w.thread所引用的线程对象, 也就是要执行 w 的 run()方法.
                    t.start();
                    workerStarted = true;
                }
            }
        } finally {
            if (!workerStarted)
                addWorkerFailed(w);
        }
        return workerStarted;
    }

    /**
     * Rolls back the worker thread creation.
     * - removes worker from workers, if present
     * - decrements worker count
     * - rechecks for termination, in case the existence of this
     * worker was holding up termination
     */
    private void addWorkerFailed(Worker w) {
        final ReentrantLock mainLock = this.mainLock;
        mainLock.lock();
        try {
            if (w != null)
                workers.remove(w);
            decrementWorkerCount();
            tryTerminate();
        } finally {
            mainLock.unlock();
        }
    }

    // Public constructors and methods

    /**
     * 对一个将要死亡的的worker进行清理和管理
     * Performs cleanup and bookkeeping for a dying worker. Called
     * only from worker threads. Unless completedAbruptly is set,
     * assumes that workerCount has already been adjusted to account
     * for exit.  This method removes thread from worker set, and
     * possibly terminates the pool or replaces the worker if either
     * it exited due to user task exception or if fewer than
     * corePoolSize workers are running or queue is non-empty but
     * there are no workers.
     *
     * @param w                 the worker
     * @param completedAbruptly if the worker died due to user exception
     */
    private void processWorkerExit(Worker w, boolean completedAbruptly) {
        if (completedAbruptly) // If abrupt, then workerCount wasn't adjusted
            decrementWorkerCount();

        final ReentrantLock mainLock = this.mainLock;
        mainLock.lock();
        try {
            completedTaskCount += w.completedTasks;
            //移除将要死亡的线程
            workers.remove(w);
        } finally {
            mainLock.unlock();
        }

        tryTerminate();

        int c = ctl.get();
        if (runStateLessThan(c, STOP)) {
            if (!completedAbruptly) {
                int min = allowCoreThreadTimeOut ? 0 : corePoolSize;
                if (min == 0 && !workQueue.isEmpty())
                    min = 1;
                if (workerCountOf(c) >= min)
                    return; // replacement not needed
            }
            addWorker(null, false);
        }
    }

    /**
     * 通过getTask返回空，来结束掉当前worker
     * Performs blocking or timed wait for a task, depending on
     * current configuration settings, or returns null if this worker
     * must exit because of any of:
     * 1. There are more than maximumPoolSize workers (due to
     * a call to setMaximumPoolSize).
     * 2. The pool is stopped.
     * 3. The pool is shutdown and the queue is empty.
     * 4. This worker timed out waiting for a task, and timed-out
     * workers are subject to termination (that is,
     * {@code allowCoreThreadTimeOut || workerCount > corePoolSize})
     * both before and after the timed wait, and if the queue is
     * non-empty, this worker is not the last thread in the pool.
     *
     * @return task, or null if the worker must exit, in which case
     * workerCount is decremented
     */
    private Runnable getTask() {
        //没有修改状态就不用加锁
        //最后一次获取任务是否超时
        boolean timedOut = false; // Did the last poll() time out?

        for (; ; ) {
            int c = ctl.get();
            int rs = runStateOf(c);

            // Check if queue empty only if necessary.
            // 如果线程池已停止, 或者线程池被关闭并且线程池内的阻塞队列为空, 则结束该方法并返回 null.
            if (rs >= SHUTDOWN && (rs >= STOP || workQueue.isEmpty())) {
                decrementWorkerCount();
                return null;
            }

            int wc = workerCountOf(c);

            // 如果 allowCoreThreadTimeOut 这个字段设置为 true(也就是允许核心线程受超时机制的控制), 则
            // 直接设置 timed 为 true. 反之, 则再看当前线程池中的有效线程数是否已经超过了核心线程数, 也
            // 就是是否存在非核心线程. 如果存在非核心线程, 那么也会设置 timed 为true.
            // 如果 wc <= corePoolSize (线程池中的有效线程数少于核心线程数, 即: 线程池内运行着的都是核心线程),
            // 并且 allowCoreThreadTimeOut 为 false(即: 核心线程即使空闲, 也不会受超时机制的限制),
            // 那么就设置 timed 为 false.
            // Are workers subject to culling?
            boolean timed = allowCoreThreadTimeOut || wc > corePoolSize;

            // 当线程池处于 RUNNING (运行)状态但阻塞队列内已经没有任务(为空)时, 将导致有线程接下来会一直
            // 处于空闲状态. 如果空闲的是核心线程并且设置核心线程不受超时机制的影响(默认情况下就是这个设置),
            // 那么这些核心线程将一直在线程池中处于空闲状态, 等待着新任务的到来, 只要线程池处于 RUNNING
            // (运行)状态, 那么, 这些空闲的核心线程将一直在池子中而不会被销毁. 如果空闲的是非核心线程, 或者
            // 虽然是核心线程但是设置了核心线程受超时机制的限制, 那么当空闲达到超时时间时, 这就满足了这里的
            // if条件而去执行 if内部的代码, 通过返回 null 结束掉该 getTask()方法, 也最终结束掉 runWorker()方法.
            if ((wc > maximumPoolSize || (timed && timedOut)) && (wc > 1 || workQueue.isEmpty())) {
                if (compareAndDecrementWorkerCount(c))
                    return null;
                continue;
            }

            try {
                // 从阻塞队列中取出队首的那个任务, 设置给 r. 如果空闲线程等待超时或者该队列已经为空, 则 r为 null.
                Runnable r = timed ?
                        workQueue.poll(keepAliveTime, TimeUnit.NANOSECONDS) :
                        workQueue.take();
                // 如果阻塞队列不为空并且未发生超时的情况, 那么取出的任务就不为 null, 就直接返回该任务对象.
                if (r != null)
                    return r;
                timedOut = true;
            } catch (InterruptedException retry) {
                timedOut = false;
            }
        }
    }

    /**
     * 循环运行，重复的从队列获取任务并且执行，在处理问题有以下几点
     * Main worker run loop.  Repeatedly gets tasks from queue and
     * executes them, while coping with a number of issues:
     * <p>
     * 1. We may start out with an initial task, in which case we
     * don't need to get the first one. Otherwise, as long as pool is
     * running, we get tasks from getTask. If it returns null then the
     * worker exits due to changed pool state or configuration
     * parameters.  Other exits result from exception throws in
     * external code, in which case completedAbruptly holds, which
     * usually leads processWorkerExit to replace this thread.
     * 在开始之前会自带一个初始任务，这样我们就不用去获取第一个任务了。
     * 我们会通过getTask获取任务，一直到现在池停止运行。
     * <p>
     *
     * 2. Before running any task, the lock is acquired to prevent
     * other pool interrupts while the task is executing, and then we
     * ensure that unless pool is stopping, this thread does not have
     * its interrupt set.
     * 在运行任何任务之前，要获取锁防止任务在执行的时候被其他线程池中断，除非线程
     * 池被停止，线程没有它的中断集合
     * <p>
     * 3.每个任务运行前，将会调用，beforeExecute方法，该方法可能会抛出异常，
     * 这会导致线程还没开始处理任务就被中断
     * 3. Each task run is preceded by a call to beforeExecute, which
     * might throw an exception, in which case we cause thread to die
     * (breaking loop with completedAbruptly true) without processing
     * the task.
     * <p>
     * 4.假设beforeExecute正常完成后，我们开始执行任务，会搜集执行之间任务抛出的异常
     * 到afterExecute。我们分开处理RuntimeException,Error和任何其他的Throwables
     * 因为我们不能在Runnable的run方法中抛出异常。我们将异常包装，以UncaughtExceptionHandler
     * 的方式抛出。
     * <p>
     * 4. Assuming beforeExecute completes normally, we run the task,
     * gathering any of its thrown exceptions to send to afterExecute.
     * We separately handle RuntimeException, Error (both of which the
     * specs guarantee that we trap) and arbitrary Throwables.
     * Because we cannot rethrow Throwables within Runnable.run, we
     * wrap them within Errors on the way out (to the thread's
     * UncaughtExceptionHandler).  Any thrown exception also
     * conservatively causes thread to die.
     * <p>
     * 5.在任务的run方法执行完后，如果线程有异常抛出，便会调用afterExecute方法，
     * 但该方法任然可能抛出异常，异常的抛出任然会导致线程死亡。
     * <p>
     * 5. After task.run completes, we call afterExecute, which may
     * also throw an exception, which will also cause thread to
     * die. According to JLS Sec 14.20, this exception is the one that
     * will be in effect even if task.run throws.
     * <p>
     * The net effect of the exception mechanics is that afterExecute
     * and the thread's UncaughtExceptionHandler have as accurate
     * information as we can provide about any problems encountered by
     * user code.
     *
     * @param w the worker
     */
    final void runWorker(Worker w) {
        Thread wt = Thread.currentThread();
        //获取worker的第一个任务，将firstTask赋值给task
        Runnable task = w.firstTask;
        w.firstTask = null;
        w.unlock(); // allow interrupts
        boolean completedAbruptly = true;
        try {
            // 由前边可知, task 就是 w.firstTask
            // 如果 task为 null, 那么就不进入该 while循环, 也就不运行该 task. 如果
            // task不为 null, 那么就执行 getTask()方法. 而getTask()方法是个无限
            // 循环, 会从阻塞队列 workQueue中不断取出任务来执行. 当阻塞队列 workQueue
            // 中所有的任务都被取完之后, 就结束下面的while循环.
            // getTask()是从阻塞队列获取任务,所以可能会阻塞在这里
            while (task != null || (task = getTask()) != null) {
                w.lock();
                // If pool is stopping, ensure thread is interrupted;
                // if not, ensure thread is not interrupted.  This
                // requires a recheck in second case to deal with
                // shutdownNow race while clearing interrupt
                if ((runStateAtLeast(ctl.get(), STOP) || (Thread.interrupted() && runStateAtLeast(ctl.get(), STOP))) && !wt.isInterrupted())
                    wt.interrupt();
                try {
                    beforeExecute(wt, task);
                    Throwable thrown = null;
                    try {
                        task.run();   //任务的run方法在这里执行
                    } catch (RuntimeException x) {
                        thrown = x;
                        throw x;
                    } catch (Error x) {
                        thrown = x;
                        throw x;
                    } catch (Throwable x) {
                        thrown = x;
                        throw new Error(x);
                    } finally {
                        afterExecute(task, thrown);   //异常抛出会执行afterExecute方法
                    }
                } finally {
                    // 将 task 置为 null, 这样使得 while循环是否继续执行的判断, 就只能依赖于判断
                    // 第二个条件, 也就是 (task = getTask()) != null 这个条件, 是否满足.
                    task = null;
                    w.completedTasks++;
                    w.unlock();
                }
            }
            //任务执行完成，completedAbruptly为false，失败为true
            completedAbruptly = false;
        } finally {
            processWorkerExit(w, completedAbruptly);
        }
    }

    /**
     * Executes the given task sometime in the future.  The task
     * may execute in a new thread or in an existing pooled thread.
     * <p>
     * If the task cannot be submitted for execution, either because this
     * executor has been shutdown or because its capacity has been reached,
     * the task is handled by the current {@code RejectedExecutionHandler}.
     *
     * @param command the task to execute
     * @throws RejectedExecutionException at discretion of
     *                                    {@code RejectedExecutionHandler}, if the task
     *                                    cannot be accepted for execution
     * @throws NullPointerException       if {@code command} is null
     */
    @Override
    public void execute(Runnable command) {
        if (command == null) {
          throw new NullPointerException();
        }
        /*
         * Proceed in 3 steps:
         *
         * 1. If fewer than corePoolSize threads are running, try to
         * start a new thread with the given command as its first
         * task.  The call to addWorker atomically checks runState and
         * workerCount, and so prevents false alarms that would add
         * threads when it shouldn't, by returning false.
         *
         * 2. If a task can be successfully queued, then we still need
         * to double-check whether we should have added a thread
         * (because existing ones died since last checking) or that
         * the pool shut down since entry into this method. So we
         * recheck state and if necessary roll back the enqueuing if
         * stopped, or start a new thread if there are none.
         *
         * 3. If we cannot queue task, then we try to add a new
         * thread.  If it fails, we know we are shut down or saturated
         * and so reject the task.
         */
        int c = ctl.get();
        if (workerCountOf(c) < corePoolSize) {
          //添加成功
            if (addWorker(command, true)) {
              return;
            }
            c = ctl.get();
        }
        //如果核心线程数量已经满了，那么就把当前任务添加到工作队列
        //程序执行到这里，表示线程要么已经没有处于RUNNING状态或者
        //当前线程的workCount大于corePoolSize
        //此时如果线程池已经不处于RUNNING状态或者提交到队列失败
        //那么则进入下一个步骤
        if (isRunning(c) && workQueue.offer(command)) {
            int recheck = ctl.get();
            if (!isRunning(recheck) && remove(command)) {
              reject(command);
            }
            // 如果线程池仍然处于运行状态，并且任务提交到阻塞队列成功
            // 再次计算线程池内的有效线程数 workerCount, 一旦发现该数量变为0,
            // 就将线程池内的线程数上限值设置为最大线程数 maximumPoolSize, 然后
            // 只是创建一个线程而不去启动它, 并结束整个 execute()方法的执行.
            else if (workerCountOf(recheck) == 0) {
              addWorker(null, false);
            }
        }
        // 如果该方法能够执行到这里, 那么结合分析1和分析2可知, 线程池此时必定是
        // 下面两种情况中的一种:
        // ① 已经不处于RUNNING(运行)状态
        // ② 处于RUNNING(运行)状态, 并且线程池内的有效线程数 workerCount已经>= 核心线程数 corePoolSize,
        // 并且线程池内的阻塞队列 workQueue已满
        // 再次执行addWorker() 方法, 将线程池内的线程数上限值设置为最大线程数
        // maximumPoolSize, 并将提交的任务 command作为被执行的对象, 尝试创建并
        // 启动一个线程来执行该任务. 如果此时线程池的状态为如下两种中的一种,
        // 就会触发 handler的 rejectedExecution()方法来拒绝该任务的执行:
        // ① 未处于RUNNING(运行)状态.
        // ② 处于RUNNING(运行)状态, 但线程池内的有效线程数已达到本次设定的最大
        // 线程数 (另外根据分析3可知, 此时线程池内的阻塞队列 workQueue已满).
        //
        // 如果线程池处于 RUNNING(运行)状态, 但有效线程数还未达到本次设定的最大
        // 线程数, 那么就会尝试创建并启动一个线程来执行任务 command. 如果线程的
        // 创建和启动都很顺利, 那么就直接结束掉该 execute()方法; 如果线程的创建或
        // 启动失败, 则同样会触发 handler的 rejectedExecution()方法来拒绝该
        // 任务的执行并结束掉该 execute()方法.
        else if (!addWorker(command, false)) {
          reject(command);
        }
    }

    /**
     * 启动shutdown前将会是已提交的队列有序执行，但是并不会接收新的任务
     * 该操作将不会对在执行的线程进行任何干预。
     * Initiates an orderly shutdown in which previously submitted
     * tasks are executed, but no new tasks will be accepted.
     * Invocation has no additional effect if already shut down.
     * <p>
     * <p>This method does not wait for previously submitted tasks to
     * complete execution.  Use {@link #awaitTermination awaitTermination}
     * to do that.
     *
     * @throws SecurityException {@inheritDoc}
     */
    public void shutdown() {
        final ReentrantLock mainLock = this.mainLock;
        mainLock.lock();
        try {
            checkShutdownAccess();
            advanceRunState(SHUTDOWN);
            interruptIdleWorkers();
            onShutdown(); // hook for ScheduledThreadPoolExecutor
        } finally {
            mainLock.unlock();
        }
        tryTerminate();
    }

    /**
     * Attempts to stop all actively executing tasks, halts the
     * processing of waiting tasks, and returns a list of the tasks
     * that were awaiting execution. These tasks are drained (removed)
     * from the task queue upon return from this method.
     * <p>
     * <p>This method does not wait for actively executing tasks to
     * terminate.  Use {@link #awaitTermination awaitTermination} to
     * do that.
     * <p>
     * <p>There are no guarantees beyond best-effort attempts to stop
     * processing actively executing tasks.  This implementation
     * cancels tasks via {@link Thread#interrupt}, so any task that
     * fails to respond to interrupts may never terminate.
     *
     * @throws SecurityException {@inheritDoc}
     */
    public List<Runnable> shutdownNow() {
        List<Runnable> tasks;
        final ReentrantLock mainLock = this.mainLock;
        mainLock.lock();
        try {
            checkShutdownAccess();
            advanceRunState(STOP);
            interruptWorkers();
            tasks = drainQueue();
        } finally {
            mainLock.unlock();
        }
        tryTerminate();
        return tasks;
    }

    public boolean isShutdown() {
        return !isRunning(ctl.get());
    }

    /**
     * Returns true if this executor is in the process of terminating
     * after {@link #shutdown} or {@link #shutdownNow} but has not
     * completely terminated.  This method may be useful for
     * debugging. A return of {@code true} reported a sufficient
     * period after shutdown may indicate that submitted tasks have
     * ignored or suppressed interruption, causing this executor not
     * to properly terminate.
     *
     * @return {@code true} if terminating but not yet terminated
     */
    public boolean isTerminating() {
        int c = ctl.get();
        return !isRunning(c) && runStateLessThan(c, TERMINATED);
    }

    public boolean isTerminated() {
        return runStateAtLeast(ctl.get(), TERMINATED);
    }

    public boolean awaitTermination(long timeout, TimeUnit unit)
            throws InterruptedException {
        long nanos = unit.toNanos(timeout);
        final ReentrantLock mainLock = this.mainLock;
        mainLock.lock();
        try {
            for (; ; ) {
                if (runStateAtLeast(ctl.get(), TERMINATED))
                    return true;
                if (nanos <= 0)
                    return false;
                nanos = termination.awaitNanos(nanos);
            }
        } finally {
            mainLock.unlock();
        }
    }

